import java.awt.Color;
import java.awt.Graphics;
import java.awt.Rectangle;
import java.awt.event.KeyEvent;


public class Snake {
	private int size=0;
	private Node head = null;
	private Node tail = null;
	
	private Node n = new Node(20,30, Dir.L);
	private Yard y;
    public Snake (Yard y) {
		head = n;
		tail = n;
		size = 1;
		this.y = y;
		
	}
    
    public void addToTail() {
		Node node = null;
		switch(tail.dir) {
		case L :
			node = new Node(tail.row, tail.col + 1, tail.dir);
			break;
		case U :
			node = new Node(tail.row + 1, tail.col, tail.dir);
			break;
		case R :
			node = new Node(tail.row, tail.col - 1, tail.dir);
			break;
		case D :
			node = new Node(tail.row - 1, tail.col, tail.dir);
			break;
		}
		tail.next = node;
		node.prev = tail;
		tail = node;
		size ++;
	}
    
    public void addToHead() {
		Node node = null;
		switch(head.dir) {
		case L :
			node = new Node(head.row, head.col - 1, head.dir);
			break;
		case U :
			node = new Node(head.row - 1, head.col, head.dir);
			break;
		case R :
			node = new Node(head.row, head.col + 1, head.dir);
			break;
		case D :
			node = new Node(head.row + 1, head.col, head.dir);
			break;
		}
		node.next = head;
		head.prev = node;
		head = node;
		size ++;
	}
   
   /*绘制蛇的图形*/
    public void draw(Graphics g) {
    	if(size <= 0) return;
    	move();
    	for(Node n = head; n != null; n = n.next){
    		n.draw(g);
    	}
    }
   
    /*蛇移动*/
    public void move() {
    	addToHead();
    	deleteFromTail();
    	checkdead();/*检测蛇没有死*/
    }
    
    
    private void checkdead() {
    	if(head.row < 2 || head.col < 0 || head.row > Yard.ROWS || head.col > Yard.CLOS)  {
			y.stop();
		}
    	/*检测蛇头部和身子相撞*/
    	for(Node n = head.next; n != null; n = n.next) {
			if(head.row == n.row && head.col == n.col) {
				y.stop();
			}
		}
	}

    /*删除蛇的尾巴*/
	public void deleteFromTail() {
    	if(size == 0) return;
    	tail = tail.prev;
    	tail.next = null;
    }
	
 	private class Node {
		int row,col;
		int width = Yard.BLOCK_SIZE;
		int height = Yard.BLOCK_SIZE;
		Dir dir = Dir.L;
		Node next = null;
		Node prev = null;
		
		Node(int row, int col, Dir dir) {
			this.row = row;
			this.col = col;
			this.dir = dir;
		}
		
		void draw (Graphics g) {
			Color c = g.getColor();
			g.setColor(Color.BLACK);
			g.fillRect(Yard.BLOCK_SIZE * col, Yard.BLOCK_SIZE * row, width, height);
			g.setColor(c);
		}			
	}
 	
 	/*碰撞检测*/ 
 	public void eat (Egg e) {
 		if(this.getRect().intersects(e.getRect())){
 			e.reAppear();/*碰到之后，蛋在新的地方重新出现*/
 			this.addToHead();/*吃掉之后长一节*/
 			y.setScore(y.getScore() + 5 );
 		}
 	}
 	/*蛇头的位置*/
 	private Rectangle getRect() {
 		return new Rectangle (Yard.BLOCK_SIZE * head.col, Yard.BLOCK_SIZE * head.row, head.row, head.col);
 	}
	
 	/*通过监听键盘的方向，改变蛇的移动方向*/
	public void keyPressed(KeyEvent e) {
		int key = e.getKeyCode();
		switch (key) {
		case KeyEvent.VK_LEFT :
			if(head.dir != Dir.R)/*为了防止蛇还可以往回走：即本来往左走，如果你按了右键，它又往右走这种情况*/
			head.dir = Dir.L ;
			break;
		case KeyEvent.VK_UP :
			if(head.dir != Dir.D)
			head.dir = Dir.U;
			break;
		case KeyEvent.VK_RIGHT :
			if(head.dir != Dir.	L)
			head.dir = Dir.R;
			break;
		case KeyEvent.VK_DOWN :
			if(head.dir != Dir.U)
			head.dir = Dir.D;
			break;
		}
	}
}


